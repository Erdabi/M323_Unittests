function sumPlus(a,b) {
   const result = a + b;
   return result;
}
function sumMinus(a,b) {
    const result = a - b;
    return result;
 }
 function sumMultiply(a,b) {
    const result = a * b;
    return result;
 }
 function sumDivide(a,b) {
    const result = a / b;
    return result;
 }
